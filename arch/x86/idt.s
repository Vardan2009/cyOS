EXTERN ISRHandler
ISR_STUB:
	PUSHA
	MOV EAX, DS
	PUSH EAX 
	MOV EAX, CR2
	PUSH EAX 

	MOV AX, 0x10
	
	MOV DS, AX
	MOV ES, AX 
	MOV FS, AX 
	MOV GS, AX

	PUSH ESP

	CALL ISRHandler

	ADD ESP, 8
	POP EBX

	MOV DS, BX
	MOV ES, BX 
	MOV FS, BX 
	MOV GS, BX

	POPA

	ADD ESP, 8
	STI 
	IRET

